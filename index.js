/**
 * Generate one Event Node, depending on the parameter 'fullDetails'
 * we will generate a simple card for the list or a big one for the
 * one event detail display
 *
 * @param {Object}   event           The event Object.
 * @param {Boolean}  fullDetails     Is it a small card or a big one.
 *
 */
const eventCreate = (event, fullDetails) => {
  let eventWrapper = document.createElement("div");

  let title = document.createTextNode(event.title);
  let titleWrapper = document.createElement("h4");
  titleWrapper.appendChild(title);

  let city = document.createTextNode(event.city);
  let cityWrapper = document.createElement("h5");
  cityWrapper.appendChild(city);

  let date = new Date(parseInt(event.date));
  let day = date.getUTCDate();
  let month = date.getMonth();
  let year = date.getFullYear();

  let dayNode = document.createTextNode(day);
  let monthNode = document.createTextNode(
    date.toLocaleString("default", { month: "short" })
  );
  let yearNode = document.createTextNode(year);

  let dayWrapper = document.createElement("span");
  dayWrapper.classList.add("day");
  dayWrapper.appendChild(dayNode);
  let MonthWrapper = document.createElement("span");
  MonthWrapper.classList.add("month");
  MonthWrapper.appendChild(monthNode);
  let yearWrapper = document.createElement("span");
  yearWrapper.classList.add("year");
  yearWrapper.appendChild(yearNode);

  let dateWrapper = document.createElement("div");
  dateWrapper.classList.add("date");
  dateWrapper.append(dayWrapper, MonthWrapper, yearWrapper);

  let type = document.createTextNode(event.type);
  let typeWrapper = document.createElement("span");
  typeWrapper.style.backgroundColor = types[event.type].standOut
    ? types[event.type].color
    : "";
  typeWrapper.style.color = !types[event.type].standOut
    ? types[event.type].color
    : "";

  typeWrapper.classList.add("type");
  typeWrapper.appendChild(type);

  const headWrapper = document.createElement("div");
  headWrapper.classList.add("eventHead");
  headWrapper.appendChild(dateWrapper);
  headWrapper.appendChild(typeWrapper);

  let descriptionWrapper;
  let detailsButtonWrapper;
  let applyButtonWrapper;

  if (fullDetails) {
    let description = document.createTextNode(event.content);
    descriptionWrapper = document.createElement("p");
    descriptionWrapper.appendChild(description);
  } else {
    let description = document.createTextNode(event.subtitle);
    descriptionWrapper = document.createElement("p");
    descriptionWrapper.appendChild(description);

    detailsButtonWrapper = document.createElement("button");
    let detailsButton = document.createTextNode("details");

    detailsButtonWrapper.appendChild(detailsButton);
    detailsButtonWrapper.classList.add("details");

    detailsButtonWrapper.addEventListener("click", () => {
      const detailEvent = eventCreate(event, true);
      displayModal(detailEvent);
    });
  }

  let shareWrapper = document.createElement("div");
  shareWrapper.classList.add("share");
  let facebook = document.createTextNode("fa");
  let linkedin = document.createTextNode("in");
  let twitter = document.createTextNode("t");

  let facebookWrapper = document.createElement("span");
  facebookWrapper.classList.add("facebook");
  let linkedinWrapper = document.createElement("span");
  linkedinWrapper.classList.add("linkedin");
  let twitterWrapper = document.createElement("span");
  twitterWrapper.classList.add("twitter");

  facebookWrapper.appendChild(facebook);
  facebookWrapper.classList.add("sn");
  linkedinWrapper.appendChild(linkedin);
  linkedinWrapper.classList.add("sn");
  twitterWrapper.appendChild(twitter);
  twitterWrapper.classList.add("sn");

  shareWrapper.appendChild(facebookWrapper);
  shareWrapper.appendChild(linkedinWrapper);
  shareWrapper.appendChild(twitterWrapper);

  let contentWrapper = document.createElement("div");
  contentWrapper.classList.add("detailedDescription");

  contentWrapper.appendChild(headWrapper);
  contentWrapper.appendChild(titleWrapper);
  contentWrapper.appendChild(cityWrapper);
  contentWrapper.appendChild(descriptionWrapper);

  const flex = document.createElement("div");
  flex.style.flex = 1;
  contentWrapper.appendChild(flex);

  const eventFooterWrapper = document.createElement("div");
  eventFooterWrapper.classList.add("footer");

  eventFooterWrapper.appendChild(shareWrapper);

  const actionWrapper = document.createElement("div");
  actionWrapper.classList.add("action");

  if (detailsButtonWrapper) {
    actionWrapper.appendChild(detailsButtonWrapper);
  }

  applyButtonWrapper = document.createElement("button");

  if (event.application) {
    let applyButton = document.createTextNode("Application");

    applyButtonWrapper.appendChild(applyButton);
    applyButtonWrapper.classList.add("apply");
    applyButtonWrapper.addEventListener("click", () => {
      displayModal(confirmationNode(event));
    });
  } else {
    let applyButton = document.createTextNode("Apply");

    applyButtonWrapper.appendChild(applyButton);
    applyButtonWrapper.classList.add("apply");
    applyButtonWrapper.addEventListener("click", () => {
      if (event.type !== "Premium-only Webinar") {
        event.application = new Date();
        displayModal(confirmationNode(event));
        refreshPage();
      } else {
        displayModal(errorWrapper);
      }
    });
  }
  actionWrapper.appendChild(applyButtonWrapper);
  eventFooterWrapper.appendChild(actionWrapper);

  contentWrapper.appendChild(eventFooterWrapper);

  let image = document.createElement("div");
  image.classList.add("img");

  image.style.backgroundImage = `url('https://placeimg.com/800/600/any?${event.image}')`;
  image.title = event.title;

  eventWrapper.appendChild(image);
  event.classNames.forEach((className) =>
    eventWrapper.classList.add(className)
  );

  eventWrapper.appendChild(contentWrapper);

  return eventWrapper;
};

/**
 * Generate all events on the page
 *
 * @param {JSON}     source           JSON object  of all events to display.
 * @param {String}   destination      ID of the html node where display events list
 *
 */
const eventsList = (source, destination) => {
  const Wrapper = document.getElementById(destination);
  Wrapper.innerHTML = "";

  source.forEach((event) => {
    let eventNode = eventCreate(event, false);
    Wrapper.appendChild(eventNode);
  });
};

/**
 * Triggers the page generation. we need itin case we want to refresh the page.
 */
const refreshPage = () => {
  eventsList(events, "list");
};

