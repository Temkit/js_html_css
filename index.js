/**
 * Open the modal with the specified content that was created before calling the
 * display modal funciton. this funciton will be used to display all the modals
 * we have in the challenge
 *
 * @param {Object}   content    Html Element
 *
 */
const displayModal = (content) => {
  modalContent.innerHTML = "";

  modalContent.appendChild(content);

  modalWrapper.addEventListener("click", (e) => {
    e.stopPropagation();
    modalWrapper.style.display = "none";
    modalContent.style.display = "none";
  });

  modalWrapper.style.display = "flex";
  modalContent.style.display = "flex";

  modalContent.addEventListener("click", (e) => {
    e.stopPropagation();
  });
};

/**
 * Close modal Wrapper function
 *
 */
const closeModal = () => {
  let closeTrigger = document.createElement("button");
  let closeTriggerText = document.createTextNode("close");
  closeTrigger.appendChild(closeTriggerText);

  closeTrigger.addEventListener("click", () => {
    modalContent.innerHTML = "";
    modalWrapper.style.display = "none";
    modalContent.style.display = "none";
  });

  closeTrigger.classList.add("apply");

  return closeTrigger;
};

